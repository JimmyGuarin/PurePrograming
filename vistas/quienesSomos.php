                    <h1>¿Quién es Subastas Pure Programing? </h1>
                    <p>Es el líder de mercado y se especializa en la recuperación de capital a través de la venta de activos inservibles y obsoletos tales como maquinaria pesada y agrícola, equipos industriales, fue pionera en realizar subastas oficiales que utilizan Internet para los sectores de automóviles y bienes raíces industriales.</p>

                    <h1>¿Qué hacemos?</h1>
                    <p>Se ofrece a los clientes servicios de evaluación y venta de activos. Para llevar a cabo se utilizan diversas estrategias de promoción de ventas y negociación.<br>
                    Nuestra especialidad es el de promover la venta de activos en subastas utilizando el Internet para hacer la vida más fácil para cualquier persona que quiere comprar. Como resultado incrementado significativamente la exposición de los productos que se ofrecen, proporcionando el acceso democrático a los compradores y que resulta en negociaciones transparentes entre las partes.<br>
                    Durante el proceso de realización de ventas, Subastas Pure/Programing es responsable de la identificación del mercado comprador y por una amplia y especializada difusión.</p>

                    <h1>¿Por qué es ventajoso? </h1>
                    <p>Porque permite la participación de compradores de diferentes lugares en igualdad de condiciones, un seguro a todo riesgo y sin pérdida de tiempo y costos adicionales de viajar para estar en el ambiente del auditorio. <br>Subastas Pure/Programing trajo a los vendedores la ventaja de una elevada liquidez para sus activos, en última instancia, lo que no es para que cada uno puede servir a los demás.</p>

                    <h1>¿Quiénes son nuestros clientes? </h1>
